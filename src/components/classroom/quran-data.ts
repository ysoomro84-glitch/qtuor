// ============================================================
// Qtuor Digital Library — Complete Noorani Qaida (17 lessons)
// and a comprehensive selection of Quran surahs (Juz Amma +
// Al-Fatihah + Ayat al-Kursi + key verses).
// Every word has a unique id so the teacher can click-to-highlight
// it in real time in the virtual classroom.
// ============================================================

export interface QuranWord {
  id: string
  text: string
  // Word-by-word enrichment from Quran.com API v4
  translation?: string      // English meaning of this specific word
  transliteration?: string  // English transliteration (e.g. "bis'mi")
  audioUrl?: string         // Per-word audio file URL
  tajweedColor?: string     // Tajweed rule color (from AlQuran.cloud)
}
export interface QuranLine {
  words: QuranWord[]
}
export type ContentType = 'quran' | 'qaida'

export interface LibraryPage {
  id: number
  type: ContentType
  // Quran fields
  surah?: string
  surahArabic?: string
  ayahRange?: string
  juz?: string
  surahNumber?: number
  // Qaida fields
  lessonNumber?: number
  lessonTitle?: string
  lessonTitleArabic?: string
  instruction?: string
  rule?: string
  // Shared
  bismillah?: boolean
  lines: QuranLine[]
}

// Helper to build a line of words quickly
function L(...words: [string, string][]): QuranLine {
  return { words: words.map(([id, text]) => ({ id, text })) }
}

